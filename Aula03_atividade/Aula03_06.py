n1 = float(input("Digite o primeiro número:"))
n2 = float(input("Digite o segundo número:"))

print(f"Adição de {n1} e {n2} é {n1+n2}")
print(f"Subtração de {n1} por {n2} é {n1-n2}")
print(f"Multiplicação de {n1} por {n2} é {n1*n2}")
print(f"Divisão de {n1} por {n2} é {n1/n2}")
print(f"Divisão Inteira de {n1} por {n2} é {n1//n2}")
print(f"Módulo de {n1} por {n2} é {n1%n2}")
